#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char	*ptr;

	ptr = (char *)b;
	while (len > 0)
	{
		*ptr = (char)c;
		ptr++;
		len--;
	}
	return (b);
}
